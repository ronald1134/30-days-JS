//um sistema usando a condição de switch para puxar meses dependendo do valor da variável
let mes = 0
switch (mes) {
    case 1:
        console.log('Estamos no mês de Janeiro (mês 1)')
        break;
    case 2:
        console.log('estamos no mês de Fevereiro (mês 2)')
        break;
    case 3:
        console.log('Estamos no mês de Março (mês 3)')
        break;
    case 4:
        console.log('Estamos no mês de Abril (mês 4)')
        break;
    case 5:
        console.log('Estamos no mês de Maio (mês 5)')
        break;
    case 6:
        console.log('Estamos no mês de Junho (mês 6)')
        break;
    case 7:
        console.log('Estamos no mês de Julho (mês 7)')
        break;
    case 8:
        console.log('Estamos no mês de Agosto (mês 8)')
        break;
    case 9:
        console.log('Estamos no mês de Setembro (mês 9)')
        break;
    case 10:
        console.log('Estamos no mês de Outubro (mês 10)')
        break;
    case 11:
        console.log('Estamos no mês de Novembro (mês 11)')
        break;
    case 12:
        console.log('Estamos no mês de Dezembro (mês 12)')
        break;
    default: console.log("Mês inválido! Digite um valor entre 1 e 12! 📝")
}
