/*testando operadores lógicos*/ 


console.log(40 === "40") // false: comparação de igualdade e tipo

console.log(50 == 50) // True: comparação de igualdade, mas sem o tipo

console.log(5 > 5)// false: porque não é maior

console.log(10 >= 5) // true: 10 é maior do que 5, mas não é igual a ele

console.log(40 < 30)// false: 40 não é menor do que 30

console.log(150 <= 200) //True: 150 é meno do 200 e não é igual a 200

console.log(20 != "20") // false: são iguais no valor, mesmo com tipos diferentes

console.log(20 !== "20") // true: são diferentes no tipo (número vs string)